  var viewport = document.querySelector(".banner-viewport");
  var track = document.querySelector("#bannerTrack");
  var prevBtn = document.querySelector(".banner-prev");
  var nextBtn = document.querySelector(".banner-next");
  var dotsWrap = document.querySelector("#bannerDots");

  if (!viewport || !track || !prevBtn || !nextBtn) {
    return;
  }

  var slides = track.querySelectorAll(".banner-slide");
  var n = slides.length;
  if (n === 0) {
    return;
  }

  var i = 0;
  var timer = null;
  var AUTO_MS = 6000;

  function go(index) {
    i = (index + n) % n;
    track.style.transform = "translateX(-" + i * 100 + "%)";
    if (dotsWrap) {
      var dots = dotsWrap.querySelectorAll(".banner-dot");
      for (var d = 0; d < dots.length; d++) {
        dots[d].setAttribute("aria-current", d === i ? "true" : "false");
      }
    }
  }

  function next() {
    go(i + 1);
  }

  function prev() {
    go(i - 1);
  }

  function startAuto() {
    stopAuto();
    timer = setInterval(next, AUTO_MS);
  }

  function stopAuto() {
    if (timer) {
      clearInterval(timer);
      timer = null;
    }
  }

  if (dotsWrap && n > 1) {
    for (var k = 0; k < n; k++) {
      (function (idx) {
        var b = document.createElement("button");
        b.type = "button";
        b.className = "banner-dot";
        b.setAttribute("aria-label", t(currentLang, "banner.dotLabel") + (idx + 1));
        b.addEventListener("click", function () {
          go(idx);
          startAuto();
        });
        dotsWrap.appendChild(b);
      })(k);
    }
    go(0);
  }

  prevBtn.addEventListener("click", function () {
    prev();
    startAuto();
  });
  nextBtn.addEventListener("click", function () {
    next();
    startAuto();
  });

  viewport.addEventListener("mouseenter", stopAuto);
  viewport.addEventListener("mouseleave", startAuto);

  var touchStartX = null;
  viewport.addEventListener(
    "touchstart",
    function (e) {
      if (e.changedTouches && e.changedTouches[0]) {
        touchStartX = e.changedTouches[0].clientX;
      }
    },
    { passive: true }
  );
  viewport.addEventListener(
    "touchend",
    function (e) {
      if (touchStartX == null || !e.changedTouches || !e.changedTouches[0]) {
        return;
      }
      var dx = e.changedTouches[0].clientX - touchStartX;
      touchStartX = null;
      if (Math.abs(dx) > 40) {
        if (dx > 0) {
          prev();
        } else {
          next();
        }
        startAuto();
      }
    },
    { passive: true }
  );

  if (n > 1) {
    startAuto();
  }

