    function buildInquiryPayload(lang) {
      var na = lang === "en" ? "N/A" : lang === "zh" ? "未提供" : "Chua cung cap";
      var leadName = nameInput.value.trim() || na;
      var leadCompany = companyInput.value.trim() || na;
      var leadPhone = phoneInput.value.trim() || na;
      var userBrief = lastUserQuestion
        ? lastUserQuestion
        : lang === "en"
          ? "Need support for model selection and quotation."
          : lang === "zh"
            ? "需要型号选型与报价支持。"
            : "Can tu van model va bao gia.";
      return {
        question: userBrief,
        name: leadName,
        company: leadCompany,
        phone: leadPhone
      };
    }

    function payloadText(payload) {
      return (
        "HDNS Inquiry\n" +
        "Question: " + payload.question + "\n" +
        "Contact name: " + payload.name + "\n" +
        "Company: " + payload.company + "\n" +
        "Phone: " + payload.phone
      );
    }

    function canNotifyNow() {
      try {
        var lastTs = Number(localStorage.getItem(notifyLockKey) || "0");
        return Date.now() - lastTs > notifyCooldownMs;
      } catch (e) {
        return true;
      }
    }

    function markNotifySent() {
      try {
        localStorage.setItem(notifyLockKey, String(Date.now()));
      } catch (e) {
        // ignore storage errors
      }
    }

    function sendInquiryEmail(payload, lang, silent) {
      if (!canNotifyNow()) {
        return Promise.resolve(false);
      }
      var bodyText = payloadText(payload);
      return fetch("https://formsubmit.co/ajax/taogobrave@gmail.com", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          Accept: "application/json"
        },
        body: JSON.stringify({
          _subject: "New inquiry from HDNS website",
          _template: "table",
          _captcha: "false",
          source: "https://taogobrave-maker.github.io/firstweb/",
          question: payload.question,
          contact_name: payload.name,
          company: payload.company,
          phone: payload.phone,
          message: bodyText
        })
      })
        .then(function (res) {
          if (!res.ok) {
            throw new Error("notify failed");
          }
          markNotifySent();
          if (!silent) {
            pushMessage(t(lang, "bot.notifySent"), "bot");
          }
          return true;
        })
        .catch(function () {
          if (!silent) {
            pushMessage(t(lang, "bot.notifyError"), "bot");
          }
          return false;
        });
    }

