    function canNotifyNow() {
      try {
        var lastTs = Number(localStorage.getItem(notifyLockKey) || "0");
        return Date.now() - lastTs > notifyCooldownMs;
      } catch (e) {
        return true;
      }
    }

    function markNotifySent() {
      try {
        localStorage.setItem(notifyLockKey, String(Date.now()));
      } catch (e) {
        // ignore storage errors
      }
    }

    function sendInquiryEmail(payload, lang, silent) {
      if (!canNotifyNow()) {
        return Promise.resolve(false);
      }
      var bodyText = payloadText(payload);
      return fetch("https://formsubmit.co/ajax/taogobrave@gmail.com", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          Accept: "application/json"
        },
        body: JSON.stringify({
          _subject: "New inquiry from HDNS website",
          _template: "table",
          _captcha: "false",
          source: "https://taogobrave-maker.github.io/firstweb/",
          question: payload.question,
          contact_name: payload.name,
          company: payload.company,
          phone: payload.phone,
          message: bodyText
        })
      })
        .then(function (res) {
          if (!res.ok) {
            throw new Error("notify failed");
          }
          markNotifySent();
          if (!silent) {
            pushMessage(t(lang, "bot.notifySent"), "bot");
          }
          return true;
        })
        .catch(function () {
          if (!silent) {
            pushMessage(t(lang, "bot.notifyError"), "bot");
          }
          return false;
        });
    }

